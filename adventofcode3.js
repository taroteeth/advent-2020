const skiDeath = (mapString) => {
    let mapArray = mapString.split('\n');

    let treeCount = 0;

    for(let i = 0; i < mapArray.length; i++){
        let currentLine = mapArray[i];
        let lineLength = currentLine.length;
        let finalIndex = lineLength - 1;
        let xCoord = 3 * i; 
        let xPos;

        if(xCoord > finalIndex){
            xPos = xCoord % lineLength;
        } else {
            xPos = xCoord;
        }

        let chosenPath = currentLine[xPos];

        if(chosenPath === '#'){
            treeCount++;
        } 
    }
    return treeCount;
}
