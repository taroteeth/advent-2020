const suddenDeath = (mapString) => {
    let mapArray = mapString.split('\n');
    let slopeArray = [
        {'x': 1, 'y': 1}, 
        {'x': 3,'y': 1},
        {'x': 5, 'y': 1},
        {'x': 7, 'y': 1},
        {'x': 1, 'y': 2}
    ];
    let finalProduct = 1;

    function calculateTrees(slope){
        let rise = slope['y'];
        let run = slope['x'];
        let treeCount = 0;
        let counter = 0; 
        let yCoord = 0;
        let xCoord = 0;

        // need to use a while loop because we will be skipping some y 
        // lines but still want to iterate
        while(yCoord < mapArray.length){
            let currentLine = mapArray[yCoord];
            let lineLength = currentLine.length;
            let finalIndex = lineLength - 1;
            let xPos;

            if(xCoord > finalIndex){
                xPos = xCoord % lineLength;
            } else {
                xPos = xCoord;
            }

            let chosenPath = currentLine[xPos];
    
            if(chosenPath === '#'){
                treeCount++;
            } 
            counter++;
            xCoord = run * counter; 
            yCoord = rise * counter; 
        }
        finalProduct *= treeCount;
    }

    for(let i = 0; i < slopeArray.length; i++){
        let slope = slopeArray[i];
        calculateTrees(slope);
    }

    return finalProduct;
}
